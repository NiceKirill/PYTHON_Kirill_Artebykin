## Описать функцию Mean(параметры), вычисляющую среднее арифметическое
## AMean = (X+Y)/2 и среднее геометрическое GMean = y/X Y двух положительных
## чисел X и Y. С помощью этой функции найти среднее арифметическое и среднее
## геометрическое для пар (A, B), (A, C), (A, D), если даны A, B, C, D.

def Mean(X, Y):

    # Проверяем, что числа положительные
    if X <= 0 or Y <= 0:
        raise ValueError("Числа должны быть положительными")

    # Вычисляем среднее арифметическое
    AMean = (X + Y) / 2

    # Вычисляем среднее геометрическое
    GMean = (X * Y) ** 0.5  # Так как мы ищем корень, используем возведение в степень 0.5

    return AMean, GMean

# Пример использования функции
A = -1
B = 9
C = 16
D = 25

# Находим средние для пар (A, B), (A, C), (A, D)
results_AB = Mean(A, B)
results_AC = Mean(A, C)
results_AD = Mean(A, D)

# Вывод результатов
print(f"Среднее арифметическое и среднее геометрическое для пар:")
print(f"(A, B) = {results_AB}")
print(f"(A, C) = {results_AC}")
print(f"(A, D) = {results_AD}")

